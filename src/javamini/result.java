public result(int questionanswered, int marks, String user, String pass) {
    initComponents();
    biquestionanswered.setText(Integer.toString(questionanswered));
    bimarks.setText(Integer.toString(marks));
    this.user = user;
    this.pass = pass;
    lbluser.setText(user); // Set the username label

    try {
        // Load the JDBC driver
        Class.forName("com.mysql.jdbc.Driver");
        // Connect to the database
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/javaquiz", "root", "")) {
            // Prepare and execute the select query
            String selectQuery = "SELECT id FROM login WHERE userid = ?";
            try (PreparedStatement selectStatement = con.prepareStatement(selectQuery)) {
                selectStatement.setString(1, user);
                try (ResultSet resultSet = selectStatement.executeQuery()) {
                    if (resultSet.next()) {
                        int userId = resultSet.getInt("id");
                        int mark= Integer.parseInt(bimarks.getText());
                        // Insert the score using another prepared statement
                        String insertQuery = "INSERT INTO score (studid, marks) VALUES (?, ?)";
                        try (PreparedStatement insertStatement = con.prepareStatement(insertQuery)) {
                            insertStatement.setInt(1, userId);
                            insertStatement.setInt(2, mark);
                            insertStatement.executeUpdate();
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "User not found!");
                        lbluser.setText("User not found");
                    }
                }
            }
        }
    } catch (ClassNotFoundException e) {
        JOptionPane.showMessageDialog(null, "MySQL JDBC Driver not found!");
        e.printStackTrace();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Database error: " + e.getMessage());
        e.printStackTrace();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "An unexpected error occurred: " + e.getMessage());
        e.printStackTrace();
    }
}

